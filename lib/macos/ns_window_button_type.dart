/// Constants that provide a way to access standard title bar buttons.
enum NSWindowButtonType {
  /// The close button.
  closeButton,

  /// The minimize button.
  miniaturizeButton,

  /// The zoom button.
  zoomButton,

  /// The toolbar button.
  toolbarButton,

  /// The document icon button.
  documentIconButton,

  /// The document versions button.
  documentVersionsButton,
}
